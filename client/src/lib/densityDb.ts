export function getClosestDensity(formula: string, wtPercent: number): { percent: number, density: number } | null {
  const table = ACID_DENSITIES[formula];
  if (!table) return null;
  
  const percents = Object.keys(table).map(Number).sort((a, b) => a - b);
  if (percents.length === 0) return null;
  
  let closest = percents[0];
  let minDiff = Math.abs(wtPercent - closest);
  
  for (let i = 1; i < percents.length; i++) {
    const diff = Math.abs(wtPercent - percents[i]);
    if (diff < minDiff) {
      minDiff = diff;
      closest = percents[i];
    }
  }
  
  return { percent: closest, density: table[closest] };
}
